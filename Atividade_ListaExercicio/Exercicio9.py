# Declaração dos valores de temperatura em Cº
temperatura_celsius1 = 30
temperatura_celsius2 = 100
temperatura_celsius3 = 0

# Variáveis das conversões de temperatura
conversor_temp1 = (temperatura_celsius1 * 1.8) + 32
conversor_temp2 = (temperatura_celsius2 * 1.8) + 32
conversor_temp3 = (temperatura_celsius3 * 1.8) + 32

# Saída dos valores convertidos
print(f"CELSIUS -> {temperatura_celsius1} Cº     |   FAHRENHEIT -> {conversor_temp1} Fº")
print(f"CELSIUS -> {temperatura_celsius2} Cº    |   FAHRENHEIT -> {conversor_temp2} Fº")
print(f"CELSIUS -> {temperatura_celsius3} Cº      |   FAHRENHEIT -> {conversor_temp3} Fº")
