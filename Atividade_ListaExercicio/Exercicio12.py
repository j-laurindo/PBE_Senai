print("--- FATORIAL DO 3 ---")  # Título do fatorial
num = 3                         # Variável do fatorial
fatorial = 1                    # Variável do resultado do fatorial
expressao = []                  # Lista  que armazena o tamanho da expressão do fatorial

for n in range(num):            # Este loop é responsável por realizar a expressão do fatorial na quantidade de vezes do fatorial
    fatorial *= n + 1
    expressao.append(n + 1)
print(f"!{num} = {expressao} = {fatorial}")     # Saída da expressão com o resultado do fatorial

